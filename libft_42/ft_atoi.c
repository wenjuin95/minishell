#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	val;
	int	neg;

	neg = 1;
	val = 0;
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			neg = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		val = val * 10 + (*nptr - '0');
		nptr++;
	}
	return (val * neg);
}
